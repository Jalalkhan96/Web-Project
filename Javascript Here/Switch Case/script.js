const grade = 'D';

switch (grade) {
    case 'A':
        console.log('A -> very Good');
        break;

    case 'B':
        console.log('B -> Good,Keep learning');
        break;

    case 'C':
        console.log('C -> Need Improvement');
        break;

    case 'D':
        console.log('D -> Hard Word Needed');
        break;
    default:
        console.log('Not Matched');
}